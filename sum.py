def compute_sum():
    # Get two integers from the user
    num1 = int(input("Enter the first number: "))
    num2 = int(input("Enter the second number: "))
    
    # Compute the sum
    total = num1 + num2
    
    # Print the sum
    print(f"The sum of {num1} and {num2} is: {total}")

# Call the function
compute_sum()
